name = input("¿Cuál es tu nombre? ")

print("Tu nombre es ",name,"\n")

age = int(input("¿Cuál es tu edad? "))

print("Tu edad es de ",age,"\n")

peso = float(input("¿Cuanto pesas? "))

print("PESO = ",peso,"\n")

alt = float(input("¿Cual es tu altura? "))

print("Altura es de ",alt,"\n")

imc = (peso) / (alt**2)

print("Eres ",name,", con una edad de ",age,", que tiene un peso de ",peso,", altura de ",alt,". Por ende su IMC es de ",imc)



